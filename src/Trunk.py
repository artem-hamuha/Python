from math import floor
from math import ceil
from math import trunc

print(int(10.6), int(10.9), int(10.3))

print(floor(10.6), floor(10.9), floor(10.3))

print(trunc(10.6), trunc(10.9), trunc(10.3))

print(trunc(-10.6), trunc(-10.9), trunc(-10.3))

print(floor(-10.6), floor(-10.9), floor(-10.3))

print(ceil(-10.6), ceil(-10.9), ceil(-10.3))